'''
AGENTES INTELIGENTES
 Un agente es cualquier cosa capaz de percibir su medioambiente con la ayuda de sensores y actuar en ese medio utilizando actuadores. 
 Un agente es racional cuando realiza la mejor acción posible a partir de los datos percibidos.

 Características
 • Autonomía
 • Reactividad
 • Iniciativa

 Consideraciones
 • Un agente percibe y actúa en un cierto ambiente, posee una arquitectura y está implementado a través de un programa.
 • Un agente ideal (omnisciente) siempre elige aquella acción mediante la cual se maximiza su logro esperado, 
   sujeta a la secuencia de percepciones recibida hasta ese momento.
 • Un agente autónomo usa su experiencia propia en lugar de usar conocimiento incorporado por su diseñador referente al ambiente.
 • Un programa de agente mapea la ruta entre percepción y acción y actualiza el estado interno de dicho agente.
 • Un agente reflejo (o reactivo) responde de inmediato a las percepciones (jugador de ping pong).

 Diseño (REAS)
 • Rendimiento
 • Entorno
 • Actuadores
 • Sensores

 Estructura
 Agente = arquitectura + programa
'''
